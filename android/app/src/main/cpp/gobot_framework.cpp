bool GobotFramework::executeAction(const std::string& name, float time,
                                    std::function<void(const std::string&, const BoneTransform&)> applyBone)
{
    auto* action = getAction(name);
    if (!action) {
        return false;
    }

    float duration = action->duration;
    float normalizedTime = time / duration;
    if (normalizedTime > 1.0f) normalizedTime = 1.0f;

    for (const auto& [boneName, frames] : action->bones) {
        // Find surrounding keyframes and interpolate
        auto it = frames.lower_bound(normalizedTime * duration);
        if (it == frames.end()) {
            if (!frames.empty()) {
                applyBone(boneName, std::prev(it)->second);
            }
        } else if (it == frames.begin()) {
            applyBone(boneName, it->second);
        } else {
            auto prev = std::prev(it);
            float t1 = prev->first;
            float t2 = it->first;
            float t = (normalizedTime * duration - t1) / (t2 - t1);

            const auto& v1 = prev->second;
            const auto& v2 = it->second;
            BoneTransform interpolated;
            for (size_t i = 0; i < std::min(v1.size(), v2.size()); ++i) {
                interpolated.push_back(v1[i] + (v2[i] - v1[i]) * t);
            }
            applyBone(boneName, interpolated);
        }
    }

    return true;
}

