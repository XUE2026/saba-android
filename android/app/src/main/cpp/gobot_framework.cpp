bool GobotFramework::loadActionFile(const std::string& path)
{
    std::ifstream file(path);
    if (!file.is_open()) {
        LOGE("Failed to open action file: %s", path.c_str());
        return false;
    }

    std::string line;
    ActionData currentAction;
    std::string currentBone;
    bool parsingAction = false;

    while (std::getline(file, line)) {
        if (line.empty() || line[0] == '#') continue;

        if (line[0] == '[') {
            if (parsingAction && !currentAction.name.empty()) {
                mActions[currentAction.name] = currentAction;
                currentAction = ActionData();
                currentBone.clear();
            }
            
            size_t end = line.find(']');
            if (end != std::string::npos) {
                currentAction.name = line.substr(1, end - 1);
                parsingAction = true;
            }
        } else if (parsingAction) {
            if (line.find("description:") == 0) {
                currentAction.description = line.substr(12);
            } else if (line.find("duration:") == 0) {
                currentAction.duration = std::stof(line.substr(9));
            } else if (line.find("bone:") == 0) {
                size_t colon = line.find(':');
                size_t dataStart = line.find(':', colon + 1);
                if (dataStart != std::string::npos) {
                    currentBone = line.substr(colon + 1, dataStart - colon - 1);
                }
            } else if (line.find("|") != std::string::npos && !currentBone.empty()) {
                // frame data: time|x|y|z|rx|ry|rz
                std::stringstream ss(line);
                std::string token;
                std::vector<float> values;
                while (std::getline(ss, token, '|')) {
                    values.push_back(std::stof(token));
                }
                if (values.size() >= 7) {
                    float time = values[0];
                    BoneTransform transform;
                    for (int i = 1; i < 7; ++i) {
                        transform.push_back(values[i]);
                    }
                    currentAction.bones[currentBone][time] = transform;
                }
            }
        }
    }

    if (parsingAction && !currentAction.name.empty()) {
        mActions[currentAction.name] = currentAction;
    }

    file.close();
    LOGI("Loaded action file: %s (%zu actions)", path.c_str(), mActions.size());
    return true;
}

bool GobotFramework::saveActionFile(const std::string& path, const ActionData& action)
{
    std::ofstream file(path);
    if (!file.is_open()) {
        LOGE("Failed to write action file: %s", path.c_str());
        return false;
    }

    file << "# Gobot Action File v1.0\n";
    file << "# Generated by Saba MMD Viewer\n\n";

    file << "[" << action.name << "]\n";
    file << "description:" << action.description << "\n";
    file << "duration:" << action.duration << "\n\n";

    for (const auto& [boneName, frames] : action.bones) {
        file << "bone:" << boneName << ":\n";
        for (const auto& [time, transform] : frames) {
            file << time;
            for (float v : transform) {
                file << "|" << v;
            }
            file << "\n";
        }
        file << "\n";
    }

    file.close();
    LOGI("Saved action file: %s", path.c_str());
    return true;
}

ActionData* GobotFramework::getAction(const std::string& name)
{
    auto it = mActions.find(name);
    if (it != mActions.end()) {
        return &it->second;
    }
    return nullptr;
}

std::vector<std::string> GobotFramework::getActionNames() const
{
    std::vector<std::string> names;
    for (const auto& [name, _] : mActions) {
        names.push_back(name);
    }
    return names;
}

