void GobotFramework::loadBuiltinActions()
{
    // Jump action
    ActionData jump;
    jump.name = "jump";
    jump.description = "Jump animation";
    jump.duration = 0.5f;
    
    KeyFrameMap leftLeg;
    leftLeg[0.0f] = {0,0,0,0,0,0};
    leftLeg[0.15f] = {0,0,0,-0.8f,0,0};
    leftLeg[0.25f] = {0,0,0,0.2f,0,0};
    leftLeg[0.5f] = {0,0,0,0,0,0};
    jump.bones["左足"] = leftLeg;
    
    KeyFrameMap rightLeg;
    rightLeg[0.0f] = {0,0,0,0,0,0};
    rightLeg[0.15f] = {0,0,0,0.8f,0,0};
    rightLeg[0.25f] = {0,0,0,-0.2f,0,0};
    rightLeg[0.5f] = {0,0,0,0,0,0};
    jump.bones["右足"] = rightLeg;
    
    KeyFrameMap center;
    center[0.0f] = {0,0,0,0,0,0};
    center[0.1f] = {0,-2,0,0,0,0};
    center[0.25f] = {0,8,0,0,0,0};
    center[0.4f] = {0,0,0,0,0,0};
    center[0.5f] = {0,0,0,0,0,0};
    jump.bones["センター"] = center;
    
    mActions["jump"] = jump;
    
    // Walk action
    ActionData walk;
    walk.name = "walk";
    walk.description = "Walk animation";
    walk.duration = 1.0f;
    
    auto createWalkCycle = [](const std::vector<float>& times, float amplitude) -> KeyFrameMap {
        KeyFrameMap frames;
        for (size_t i = 0; i < times.size(); ++i) {
            float phase = 3.14159f * 2.0f * times[i] / 1.0f;
            float value = amplitude * std::sin(phase);
            frames[times[i]] = {0, 0, 0, value, 0, 0};
        }
        return frames;
    };
    
    std::vector<float> walkTimes = {0.0f, 0.25f, 0.5f, 0.75f, 1.0f};
    walk.bones["左腕"] = createWalkCycle(walkTimes, -0.3f);
    walk.bones["右腕"] = createWalkCycle(walkTimes, 0.3f);
    walk.bones["左足"] = createWalkCycle(walkTimes, -0.3f);
    walk.bones["右足"] = createWalkCycle(walkTimes, 0.3f);
    
    mActions["walk"] = walk;
    
    // Run action
    ActionData run;
    run.name = "run";
    run.description = "Run animation";
    run.duration = 0.5f;
    
    std::vector<float> runTimes = {0.0f, 0.125f, 0.25f, 0.375f, 0.5f};
    run.bones["左腕"] = createWalkCycle(runTimes, -0.6f);
    run.bones["右腕"] = createWalkCycle(runTimes, 0.6f);
    run.bones["左足"] = createWalkCycle(runTimes, -0.6f);
    run.bones["右足"] = createWalkCycle(runTimes, 0.6f);
    
    mActions["run"] = run;
    
    LOGI("Loaded %zu builtin actions", mActions.size());
}

